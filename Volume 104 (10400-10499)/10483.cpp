int sort_function(const void *a, const void *b){
	return (*(int *)a - *(int *)b);
}

inline void outprint(double s, double a, double b, double c){
	s *= 0.01; a *= 0.01; b *= 0.01; c *= 0.01;
	printf("%.2lf = %.2lf + %.2lf + %.2lf = %.2lf * %.2lf * %.2lf\n", s, a, b, c, a, b, c);
}

